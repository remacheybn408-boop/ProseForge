def count_sensory_dimensions(text: str) -> Dict[str, Dict[str, float]]:
    """统计六维感官词命中次数与每千汉字密度。

    Returns: {"视觉": {"count": N, "density": M}, ...}
    """
    total_chars = max(len(re.findall(r'[一-鿿]', text)), 1)
    result: Dict[str, Dict[str, float]] = {}
    for sense, patterns in SENSORY_PATTERNS.items():
        count = 0
        for pat in patterns:
            count += len(re.findall(pat, text))
        density = round(count / total_chars * 1000, 2)
        result[sense] = {"count": count, "density": density}
    return result


def detect_scene_anchors(text: str) -> int:
    """统计场景锚点命中数量（concrete_anchor_guard 使用）。"""
    return len(SCENE_ANCHOR_PATTERN.findall(text))
